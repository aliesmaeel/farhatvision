    const form = document.getElementById('contactForm');
    const phoneInputField = document.querySelector("#phone");

    // initialize intl-tel-input
    const iti = window.intlTelInput(phoneInputField, {
        initialCountry: "auto",
        geoIpLookup: function(success, failure) {
            fetch("https://ipapi.co/json/") // free geo service, no token needed
                .then(res => res.json())
                .then(data => success(data.country_code))
                .catch(() => success("us"));
        },
        utilsScript: "https://cdnjs.cloudflare.com/ajax/libs/intl-tel-input/17.0.19/js/utils.js"
    });

    form.addEventListener('submit', function(e) {
        e.preventDefault();

        // get full international phone number
        const fullPhone = iti.getNumber();

        const formData = {
            name: form.name.value,
            phone: fullPhone,
            message: form.message.value
        };

        fetch("https://script.google.com/macros/s/AKfycbxu4mSoru8z5wkTKAQYJf0a7aqWAfunrDfXLoziWvbWbLlOrVTsHb798Of7QzvWj4Eq/exec", {
            method: "POST",
            body: JSON.stringify(formData)
        })
            .then(res => res.json())
            .then(data => {
                if (data.result === "success") {
                    alert("✅ Great! Your form has been submitted successfully.");
                    form.reset();
                    iti.setCountry("us"); // reset to default after submit
                } else {
                    alert("⚠️ Something went wrong. Try again.");
                }
            })
            .catch(err => alert("Error: " + err));
    });
